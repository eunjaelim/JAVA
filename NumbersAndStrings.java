package javatutorial;

public class NumbersAndStrings {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		int z = x + y;
		System.out.println(z);
		
		String x1 = "10";
		String y1 = "20";
		String z1 = x1 + y1;
		System.out.println(z1);
		
		String x3 = "10";
		int y3 = 20;
		String z3 = x3 + y3;
		System.out.println(z3);

	}

}
